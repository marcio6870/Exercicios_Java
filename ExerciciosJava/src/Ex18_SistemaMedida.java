public class Ex18_SistemaMedida {
    public static void main(String[] args) {
       String sigla = "A";

        if(sigla == "p"){
            System.out.println("PEQUENO");
        }else if(sigla == "M"){
            System.out.println("MEDIO");
        }else if(sigla == "G"){
            System.out.println("GRANDE");
        }else{
            System.out.println("INDEFINIDO");
        }

    }
}
